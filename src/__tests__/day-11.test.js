import { expand } from '../day-11.js';
import { arrayify } from '../utils.js';

describe('expand', () => {
  it('basic', () => {
    const input = `
...#......
.......#..
#.........
..........
......#...
.#........
.........#
..........
.......#..
#...#.....`;
    expect(
      expand(arrayify(input.trim()))
        .map((l) => l.join(''))
        .join('\n'),
    ).toEqual(
      `
....#........
.........#...
#............
.............
.............
........#....
.#...........
............#
.............
.............
.........#...
#....#.......
    `.trim(),
    );
  });
});
